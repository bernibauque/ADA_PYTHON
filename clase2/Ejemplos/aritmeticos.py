# Ejemplos de operadores aritméticos en Python

# Definimos dos variables
a = 10
b = 3

# Suma
resultado_suma = a + b
print(f"Suma: {a} + {b} = {resultado_suma}")  # 10 + 3 = 13

# Resta
resultado_resta = a - b
print(f"Resta: {a} - {b} = {resultado_resta}")  # 10 - 3 = 7

# Multiplicación
resultado_multiplicacion = a * b
print(f"Multiplicación: {a} * {b} = {resultado_multiplicacion}")  # 10 * 3 = 30

# División (devuelve un número de punto flotante)
resultado_division = a / b
print(f"División: {a} / {b} = {resultado_division}")  # 10 / 3 = 3.3333333333333335

# División Entera (devuelve la parte entera del cociente)
resultado_division_entera = a // b
print(f"División Entera: {a} // {b} = {resultado_division_entera}")  # 10 // 3 = 3

# Módulo (residuo de la división)
resultado_modulo = a % b
print(f"Módulo: {a} % {b} = {resultado_modulo}")  # 10 % 3 = 1

# Exponente (potenciación)
resultado_exponente = a ** b
print(f"Exponente: {a} ** {b} = {resultado_exponente}")  # 10 ** 3 = 1000



