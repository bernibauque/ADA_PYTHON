# Ejemplos de operadores de asignacion en Python

# Asignación simple
x = 10
print(f"x = {x}")  # x = 10

# Suma y asignacion
x += 5  # Equivale a: x = x + 5
print(f"x += 5: {x}")  # x = 15

# Resta y asignacion
x -= 3  # Equivale a: x = x - 3
print(f"x -= 3: {x}")  # x = 12

# Multiplicacion y asignacion
x *= 4  # Equivale a: x = x * 4
print(f"x *= 4: {x}")  # x = 48

# Division y asignacion
x /= 2  # Equivale a: x = x / 2
print(f"x /= 2: {x}")  # x = 24.0 (resultado en punto flotante)

# Division entera y asignacion
x //= 3  # Equivale a: x = x // 3
print(f"x //= 3: {x}")  # x = 8.0 (resultado en punto flotante, división entera)

# Modulo y asignacion
x %= 4  # Equivale a: x = x % 4
print(f"x %= 4: {x}")  # x = 0.0 (residuo de la división)

# Exponente y asignacion
x **= 2  # Equivale a: x = x ** 2
print(f"x **= 2: {x}")  # x = 0.0 (0 elevado a la potencia de 2)




