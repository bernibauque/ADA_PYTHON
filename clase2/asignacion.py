# Ejemplos de operadores de asignacion

# Asignacion Simple
x = 10
print(f"x = {x}")

# Suma y asignacion
x += 5
print(f"x += 5: {x}")

# Resta y asignacion
x -= 3
print(f"x -= 3: {x}")

# Multiplicacion y asignacion 
x *= 4 # Equivale a x = x * 4
print(f"x *= 4: {x}")

# Division y asignacion 
x /= 2
print(f"x =/ 2: {x}")

# Division entera y asignacion
x //= 3
print(f"x //= 3: {x}")

# Modulo y asignacion 
x %= 7
print(f"x %= 7: {x}")

# Exponente y asignacion 
x **= 2
print(f"x **= 2: {x}")