# Ejemplo de Lista Vacía
lista_vacia = []
print("Lista Vacía:", lista_vacia)

# Ejemplo de Lista con Elementos Iniciales
lista_elementos = [1, 2, 3, 4, 5]
print("Lista con Elementos Iniciales:", lista_elementos)

# Ejemplo de Lista de Cadenas
lista_cadenas = ["manzana", "banana", "cereza"]
print("Lista de Cadenas:", lista_cadenas)

# Ejemplo de Lista de Números
lista_numeros = [10, 20, 30, 40.5]
print("Lista de Números:", lista_numeros)

# Ejemplo de Lista Mixta
lista_mixta = [42, "texto", 3.14, [1, 2, 3]]
print("Lista Mixta:", lista_mixta)

# Ejemplo de Lista con Elementos Repetidos
lista_repetidos = [0] * 5
print("Lista con Elementos Repetidos:", lista_repetidos)

# Ejemplo de Lista a Partir de Otros Iterables
cadena = "hola"
lista_de_caracteres = list(cadena)
print("Lista a Partir de Otros Iterables (cadena):", lista_de_caracteres)

