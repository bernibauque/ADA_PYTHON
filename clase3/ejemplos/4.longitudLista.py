# Lista vacía
# La longitud de una lista vacía es 0.
lista_vacia = []
print("Longitud de lista_vacia:", len(lista_vacia))  # Devuelve 0

# Lista con enteros
# La longitud es igual al número de elementos en la lista.
lista_enteros = [1, 2, 3, 4, 5]
print("Longitud de lista_enteros:", len(lista_enteros))  # Devuelve 5

# Lista con cadenas
# La longitud es igual al número de elementos en la lista.
lista_cadenas = ["manzana", "banana", "cereza"]
print("Longitud de lista_cadenas:", len(lista_cadenas))  # Devuelve 3

# Lista mixta
# La longitud cuenta todos los elementos, sin importar el tipo (números, cadenas, listas, etc.)
lista_mixta = [1, "texto", 3.14, [1, 2]]
print("Longitud de lista_mixta:", len(lista_mixta))  # Devuelve 4

# Lista con elementos repetidos
# La longitud es el resultado de multiplicar el número de elementos repetidos.
lista_repetidos = [0] * 10
print("Longitud de lista_repetidos:", len(lista_repetidos))  # Devuelve 10

