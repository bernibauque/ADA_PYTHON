# Crear un diccionario con algunos elementos
mi_diccionario = {
    "nombre": "Lucía",
    "edad": 28,
    "profesión": "Ingeniera"
}

# Imprimir el diccionario original
print("Diccionario original:", mi_diccionario)

# Usar el método .clear() para eliminar todos los elementos
mi_diccionario.clear()

# Imprimir el diccionario después de usar .clear()
print("Diccionario después de usar .clear():", mi_diccionario)  # Output: {}


