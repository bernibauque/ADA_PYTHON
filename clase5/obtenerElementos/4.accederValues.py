# Crear un diccionario con información de una persona
persona = {
    "nombre": "Carlos",
    "edad": 25,
    "ciudad": "Barcelona"
}

# Usar el método .values() para obtener todos los valores del diccionario
valores = persona.values()

# Imprimir los valores obtenidos
print("Valores del diccionario:", valores)

# Convertir los valores a una lista para mostrar cómo se verían en ese formato
valores_lista = list(valores)
print("Valores como lista:", valores_lista)

